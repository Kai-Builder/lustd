--- basic example of what this can do.
---
--- this creates a stream. This stream holds data for a temporary amount of time.
--- local printer = std.basic_stream()
---
--- Writing to the stream adds it to the stream and prints.
--- std.write(printer, "Hello, World!")
---
---
--- You can also use std.push_to, to independently add data to the stream.

